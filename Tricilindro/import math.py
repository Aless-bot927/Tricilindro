import math

def volumen_tricilindro(r):
    volumen = (8 * math.pi * r**3) / 3
    return volumen

# Ingresar el valor del radio
r = float(input("Ingresa el valor del radio: "))

# Calcular y mostrar el volumen
volumen = volumen_tricilindro(r)
print(f"El volumen del tricilindro es aproximadamente: {volumen:.3f}")
